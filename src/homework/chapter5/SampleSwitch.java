package homework.chapter5;

public class SampleSwitch {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            switch (i) {
                case 0:
                    System.out.println("i havasare 0");
                    break;
                case 1:
                    System.out.println("i havasare 1");
                    break;
                case 2:
                    System.out.println("i havasare 2");
                    break;
                case 3:
                    System.out.println("i havasare 3");
                    break;
                case 4:
                    System.out.println("i mece 4 ic");
                    break;
                case 5:
                    System.out.println("i mece 5 ic");
                    break;
                case 6:
                    System.out.println("i mece 6 ic");
                    break;
                default:
                    System.out.println("i mece 15 ic");
            }
        }
    }
}
